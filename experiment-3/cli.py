def display_bytecode(plan: ExecutionPlan) -> None:
    from compiler import bytecode_disassemble
    from brainfuck_ext import BFOp

    console.print(Rule(f" [{C_BYTE}]Stage 5 — Bytecode Disassembly[/{C_BYTE}] ", style=C_BYTE))

    disasm_lines = bytecode_disassemble(plan.bytecode).split("\n")
    total = len(disasm_lines)

    # Colour-code disassembly lines
    coloured: list[str] = []
    for line in disasm_lines[:30]:
        if "EMIT" in line:
            coloured.append(f"[bold {C_MX}]{line}[/bold {C_MX}]")
        elif "LOAD_PEM" in line or "SET_RECV" in line or "SET_AMOUNT" in line:
            coloured.append(f"[{C_PARSE}]{line}[/{C_PARSE}]")
        elif "NOP" in line:
            coloured.append(f"[{C_DIM}]{line}[/{C_DIM}]")
        elif "LOOP" in line:
            coloured.append(f"[{C_BFPP}]{line}[/{C_BFPP}]")
        elif "INC" in line or "DEC" in line:
            coloured.append(f"[{C_SYM}]{line}[/{C_SYM}]")
        elif "RIGHT" in line or "LEFT" in line:
            coloured.append(f"[{C_AST}]{line}[/{C_AST}]")
        else:
            coloured.append(line)

    if total > 35:
        coloured.append(f"[{C_DIM}]  ...  ({total - 35} instructions omitted)  ...[/{C_DIM}]")
        for line in disasm_lines[-5:]:
            coloured.append(f"[bold {C_MX}]{line}[/bold {C_MX}]")

    console.print(Panel(
        "\n".join(coloured),
        title=f"[{C_BYTE}]Bytecode[/{C_BYTE}]  [dim]({total} instructions)[/dim]",
        border_style=C_BYTE,
        box=box.ROUNDED,
    ))

    # Opcode frequency table
    from collections import Counter
    freq = Counter(BFOp(op).name for op, _ in plan.bytecode)

    freq_table = Table(
        title=f"[{C_BYTE}]Opcode Frequency[/{C_BYTE}]",
        box=box.SIMPLE,
        header_style=C_HEAD,
    )
    freq_table.add_column("Opcode", style=C_BYTE, no_wrap=True, min_width=18)
    freq_table.add_column("Count", style="bold", no_wrap=True, width=8)
    freq_table.add_column("Bar", style=C_DIM, min_width=20)

    max_count = max(freq.values()) if freq else 1
    for op_name, count in sorted(freq.items(), key=lambda x: -x[1])[:12]:
        bar_len = int(20 * count / max_count)
        bar = "█" * bar_len
        color = C_BFPP if op_name in ("INC", "DEC", "RIGHT", "LEFT", "LOOP_START", "LOOP_END") else C_WARN
        freq_table.add_row(op_name, f"[{color}]{count}[/{color}]", f"[{color}]{bar}[/{color}]")

    console.print(freq_table)
    console.print()


