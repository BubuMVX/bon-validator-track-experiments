def display_ir(ir) -> None:
    console.print(Rule(f" [{C_IR}]Stage 2 — Three-Address Intermediate Representation[/{C_IR}] ", style=C_IR))

    # Colour-code IR opcodes by category
    lines = ir.pretty().split("\n")
    coloured: list[str] = []
    for line in lines:
        if "LOAD_" in line:
            coloured.append(f"[{C_PARSE}]{line}[/{C_PARSE}]")
        elif "VALIDATE_" in line:
            coloured.append(f"[{C_WARN}]{line}[/{C_WARN}]")
        elif "ENCODE_AMOUNT_HEX" in line or "ENCODE_AMOUNT_B64" in line or "DECODE_AMOUNT" in line:
            coloured.append(f"[{C_SYM}]{line}[/{C_SYM}]")
        elif "ENCODE_AMOUNT" in line:
            coloured.append(f"[{C_GAS}]{line}[/{C_GAS}]")
        elif "SET_" in line:
            coloured.append(f"[{C_BFPP}]{line}[/{C_BFPP}]")
        elif "EMIT" in line:
            coloured.append(f"[bold {C_MX}]{line}[/bold {C_MX}]")
        elif "IRProgram" in line or "}" in line:
            coloured.append(f"[{C_DIM}]{line}[/{C_DIM}]")
        else:
            coloured.append(line)

    console.print(Panel(
        "\n".join(coloured),
        title=f"[{C_IR}]IRProgram[/{C_IR}]  [dim]({len(ir)} instructions)[/dim]",
        border_style=C_IR,
        box=box.ROUNDED,
    ))

    # Category breakdown
    cats = {
        "Loading": sum(1 for x in ir if x.op.name.startswith("LOAD_") or x.op.name.startswith("SET_")),
        "Validation": sum(1 for x in ir if x.op.name.startswith("VALIDATE_")),
        "Encoding": sum(1 for x in ir if "ENCODE" in x.op.name or "DECODE" in x.op.name),
        "Emission": sum(1 for x in ir if x.op.name == "EMIT"),
    }
    cols = []
    for cat, n in cats.items():
        color = {
            "Loading": C_PARSE,
            "Validation": C_WARN,
            "Encoding": C_SYM,
            "Emission": C_MX,
        }[cat]
        cols.append(Panel(
            Align(f"[bold {color}]{n}[/bold {color}]", align="center"),
            title=f"[{color}]{cat}[/{color}]",
            border_style=color,
            width=16,
        ))
    console.print(Columns(cols, padding=(0, 1)))
    console.print()


