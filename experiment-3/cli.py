def display_bf_source(bf_source: str) -> None:
    console.print(Rule(f" [{C_BFPP}]Stage 3 — Brainfuck++ Source Code[/{C_BFPP}] ", style=C_BFPP))

    lines = bf_source.split("\n")
    total = len(lines)

    # Count instruction types
    std_chars = sum(bf_source.count(c) for c in "+-><[].,")
    ext_count = bf_source.count("@")
    nop_count = bf_source.count("@NOP")
    emit_count = bf_source.count("@EMIT")

    # Show a preview (first 25 lines + last 8)
    if total > 40:
        preview_lines = lines[:20] + [
            f"",
            f"  [dim]┄┄┄  {total - 28} lines omitted (tape ceremony)  ┄┄┄[/dim]",
            f"",
        ] + lines[-8:]
        preview = "\n".join(preview_lines)
    else:
        preview = bf_source

    console.print(Panel(
        Syntax(preview, "text", theme="monokai", word_wrap=False),
        title=f"[{C_BFPP}]BF++ Source[/{C_BFPP}]  [dim]({total} lines)[/dim]",
        border_style=C_BFPP,
        box=box.ROUNDED,
    ))

    # Stats row
    stat_table = Table(box=None, show_header=False, padding=(0, 2))
    stat_table.add_column(style=C_DIM)
    stat_table.add_column(style="bold")

    stat_table.add_row("Standard BF chars", f"[{C_BFPP}]{std_chars}[/{C_BFPP}]")
    stat_table.add_row("Extended @ ops", f"[{C_WARN}]{ext_count}[/{C_WARN}]")
    stat_table.add_row("NOP instructions", f"[{C_DIM}]{nop_count}[/{C_DIM}]")
    stat_table.add_row("EMIT instructions", f"[{C_MX}]{emit_count}[/{C_MX}]")
    stat_table.add_row("Total source lines", f"[{C_BYTE}]{total}[/{C_BYTE}]")

    console.print(Padding(stat_table, (0, 2)))
    console.print()


