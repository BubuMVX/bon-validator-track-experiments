def _make_pipeline_table(
        statuses: list[str],  # "pending" | "running" | "done" | "error"
        summaries: list[str],
) -> Table:
    table = Table(box=box.SIMPLE_HEAVY, show_header=True, header_style=C_HEAD, expand=True)
    table.add_column("#", width=3, no_wrap=True)
    table.add_column("Stage", width=10, no_wrap=True)
    table.add_column("Description", min_width=34)
    table.add_column("Status", width=12, no_wrap=True)
    table.add_column("Output", min_width=22)

    for i, (tag, desc, color) in enumerate(PIPELINE_STAGES):
        status = statuses[i] if i < len(statuses) else "pending"
        summary = summaries[i] if i < len(summaries) else ""

        if status == "done":
            icon = f"[bold {C_MX}]✓[/bold {C_MX}]"
            s_text = f"[{C_MX}]done[/{C_MX}]"
        elif status == "running":
            icon = f"[bold yellow]⋯[/bold yellow]"
            s_text = f"[bold yellow]running[/bold yellow]"
        elif status == "error":
            icon = f"[{C_ERR}]✗[/{C_ERR}]"
            s_text = f"[{C_ERR}]error[/{C_ERR}]"
        else:
            icon = f"[{C_DIM}]·[/{C_DIM}]"
            s_text = f"[{C_DIM}]pending[/{C_DIM}]"

        table.add_row(
            f"{icon}",
            f"[{color}]{tag}[/{color}]",
            f"[{color if status != 'pending' else C_DIM}]{desc}[/{color if status != 'pending' else C_DIM}]",
            s_text,
            f"[{C_DIM}]{summary}[/{C_DIM}]",
        )

    return table


def run_compilation_with_live(ast: ASTTransactionSpec, verbose: bool) -> ExecutionPlan:
    """
    Execute the compilation pipeline while displaying an animated live table
    that updates each stage's status in real time.
    """
    statuses = ["pending"] * len(PIPELINE_STAGES)
    summaries = [""] * len(PIPELINE_STAGES)

    compiler = MasterCompiler(verbose=verbose)

    console.print(Rule(f" [{C_HEAD}]Compilation Pipeline[/{C_HEAD}] ", style="bold white"))

    with Live(
            _make_pipeline_table(statuses, summaries),
            console=console,
            refresh_per_second=20,
            vertical_overflow="visible",
    ) as live:

        def update(i: int, status: str, summary: str = "") -> None:
            statuses[i] = status
            summaries[i] = summary
            live.update(_make_pipeline_table(statuses, summaries))

        def step(i: int, fn, label: str = "") -> object:
            update(i, "running")
            time.sleep(0.08)  # theatrical pause
            try:
                result = fn()
                update(i, "done", label or "")
                return result
            except Exception as e:
                update(i, "error", str(e)[:30])
                raise

        # Stage 0 — tokenization (already done in parse_spec, simulate separately)
        step(0, lambda: time.sleep(0.05), f"{len(ast.wallet_path)} chars lexed")

        # Stage 1 — AST (already parsed, just show the node count)
        step(1, lambda: time.sleep(0.04), "7 fields")

        # Stages 2–9 — actual compilation
        plan = step(2, lambda: compiler.compile(ast), "")
        # Update summaries from actual compiler stages
        stage_map = [
            (2, "IR Generation"),
            (3, "IR Validation"),
            (4, "BF++ Emission"),
            (5, "Optimization: Loop Unrolling"),
            (6, "De-optimization: Expand MULTI_INC"),
            (7, "Bytecode Compilation"),
            (8, "Symbolic Execution Analysis"),
            (9, "Gas Estimation Heuristic"),
        ]
        for live_idx, stage_name in stage_map:
            match = next((s for s in plan.stages if s.name == stage_name), None)
            if match:
                summaries[live_idx] = match.summary
                statuses[live_idx] = "done"
        live.update(_make_pipeline_table(statuses, summaries))
        time.sleep(0.1)

    console.print()
    return plan


