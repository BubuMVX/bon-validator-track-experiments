def display_ast(ast: ASTTransactionSpec) -> None:
    console.print(Rule(f" [{C_AST}]Stage 1 — Abstract Syntax Tree[/{C_AST}] ", style=C_AST))

    tree = Tree(f"[bold {C_AST}]ASTTransactionSpec[/bold {C_AST}]  [dim](product type, 7 fields)[/dim]")

    fields = [
        ("operation", repr(ast.operation), "String", C_PARSE),
        ("token", repr(ast.token), "String", C_BFPP),
        ("amount", str(ast.amount), "Decimal", C_OK),
        ("wallet_path", repr(ast.wallet_path), "Path", C_WARN),
        ("receiver", repr(ast.receiver[:24] + "…"), "Bech32Address", C_MX),
        ("gas_limit", str(ast.gas_limit), "ℕ", C_GAS),
        ("memo", repr(ast.memo[:32]), "String", C_SYM),
    ]

    for name, val, typ, color in fields:
        tree.add(
            f"[{C_DIM}]{name:<12}[/{C_DIM}] : "
            f"[{color}]{val}[/{color}]"
            f"  [dim italic]{typ}[/dim italic]"
        )

    atto = int(ast.amount)
    info = tree.add(f"[dim]── derived ──[/dim]")
    info.add(f"[{C_DIM}]attoEGLD     :[/{C_DIM}] [{C_GAS}]{atto}[/{C_GAS}]")
    info.add(f"[{C_DIM}]hex_atto     :[/{C_DIM}] [{C_BYTE}]{hex(atto)}[/{C_BYTE}]")
    info.add(f"[{C_DIM}]memo_bytes   :[/{C_DIM}] [{C_SYM}]{len(ast.memo.encode())} bytes[/{C_SYM}]")

    console.print(Panel(tree, title=f"[{C_AST}]AST[/{C_AST}]", border_style=C_AST, box=box.ROUNDED))
    console.print()


