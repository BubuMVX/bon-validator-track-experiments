def cmd_run(args: argparse.Namespace) -> None:
    display_banner()

    # ── Résolution du spec ─────────────────────────────────────────────────────
    with Status(f"[{C_PARSE}]Resolving FTIDL specification source…[/{C_PARSE}]", console=console):
        time.sleep(0.1)
        if args.spec:
            spec_path = Path(args.spec)
            if not spec_path.exists():
                console.print(f"  {CROSS} [{C_ERR}]Spec file not found:[/{C_ERR}] {args.spec}")
                sys.exit(1)
            spec_source = spec_path.read_text(encoding="utf-8")
            if args.wallet and args.receiver and args.amount:
                spec_source = synthesize_spec(args.wallet, args.receiver, str(args.amount), args.gas_limit, args.memo)
                source_label = "synthesized from CLI (overrides spec file)"
            else:
                source_label = f"loaded from {args.spec}"
        else:
            for flag, name in [(args.wallet, "--wallet"), (args.receiver, "--receiver"), (args.amount, "--amount")]:
                if not flag:
                    console.print(f"  {CROSS} [{C_ERR}]Missing required argument:[/{C_ERR}] {name}")
                    sys.exit(1)
            spec_source = synthesize_spec(args.wallet, args.receiver, str(args.amount), args.gas_limit, args.memo)
            source_label = "synthesized from CLI arguments"

    console.print(f"  {TICK} [{C_PARSE}]Specification ready[/{C_PARSE}]  [{C_DIM}]({source_label})[/{C_DIM}]")
    console.print()

    # ── Affichage spec (verbose) ────────────────────────────────────────────────
    if args.verbose:
        display_spec(spec_source)

    # ── Parsing ────────────────────────────────────────────────────────────────
    with Status(f"[{C_PARSE}]Lexing and parsing FTIDL source…[/{C_PARSE}]", console=console):
        try:
            ast = parse_spec(spec_source)
        except Exception as e:
            console.print(f"\n  {CROSS} [{C_ERR}]Parse error:[/{C_ERR}] {e}")
            sys.exit(1)

    atto = int(ast.amount)
    console.print(
        f"  {TICK} [{C_AST}]AST produced[/{C_AST}]  "
        f"[{C_DIM}]amount=[/{C_DIM}][{C_MX}]{ast.amount} attoEGLD[/{C_MX}]  "
        f"[{C_DIM}]({atto} attoEGLD)[/{C_DIM}]  "
        f"[{C_DIM}]gas=[/{C_DIM}][{C_GAS}]{ast.gas_limit}[/{C_GAS}]"
    )

    # ── Avertissements spec ────────────────────────────────────────────────────
    if ast.gas_limit % 2 == 0:
        console.print(
            f"  {WARN} [{C_WARN}]CI-9 (Cosmological Gas Parity):[/{C_WARN}] "
            f"[dim]gas={ast.gas_limit} is even → will be adjusted to {ast.gas_limit + 1}[/dim]"
        )
    import math
    def _is_prime(n):
        if n < 2: return False
        for i in range(2, int(math.isqrt(n)) + 1):
            if n % i == 0: return False
        return True

    memo_len = len(ast.memo.encode())
    if not _is_prime(memo_len):
        from ir import _next_prime_gte
        next_p = _next_prime_gte(memo_len)
        console.print(
            f"  {WARN} [{C_WARN}]CI-7 (Prime Memo Length):[/{C_WARN}] "
            f"[dim]memo byte length {memo_len} is not prime → padding to {next_p}[/dim]"
        )
    console.print()

    # ── Pipeline de compilation animé ──────────────────────────────────────────
    t_compile_start = time.perf_counter()
    try:
        plan = run_compilation_with_live(ast, verbose=args.verbose)
    except Exception as e:
        console.print(f"\n  {CROSS} [{C_ERR}]Compilation failed:[/{C_ERR}] {e}")
        sys.exit(1)
    compile_ms = (time.perf_counter() - t_compile_start) * 1000

    console.print(
        f"  {TICK} [bold {C_MX}]Compilation complete[/bold {C_MX}]  "
        f"[yellow]{compile_ms:.1f}ms[/yellow]  "
        f"[{C_BYTE}]{plan.instruction_count()} bytecode instructions[/{C_BYTE}]  "
        f"[{C_GAS}]{plan.estimated_gas:.0f} mythical gas[/{C_GAS}]"
    )
    console.print()

    # ── Représentations intermédiaires (verbose / trace) ───────────────────────
    if args.verbose:
        display_ast(ast)
        display_ir(plan.ir)

    if args.trace or args.verbose:
        display_bf_source(plan.bf_source)
        display_optimization_stats(plan)
        display_bytecode(plan)
        display_symbolic(plan)
        display_gas_estimate(plan)

    # ── Résumé pipeline (toujours affiché) ────────────────────────────────────
    display_pipeline_summary(plan)

    # ── Exécution VM ──────────────────────────────────────────────────────────
    console.print(Rule(f" [{C_VM}]Stage 9 — BF++ Virtual Machine Execution[/{C_VM}] ", style=C_VM))

    with Progress(
            SpinnerColumn(spinner_name="dots2", style=C_VM),
            TextColumn(f"[{C_VM}]{{task.description}}[/{C_VM}]"),
            BarColumn(bar_width=30, style=C_VM, complete_style=C_MX),
            TextColumn(f"[{C_DIM}]{{task.fields[detail]}}[/{C_DIM}]"),
            TimeElapsedColumn(),
            console=console,
    ) as progress:
        vm_task = progress.add_task(
            "Initializing tape (30,000 cells)…",
            total=5,
            detail="",
        )

        time.sleep(0.05)
        progress.update(vm_task, advance=1, description="Loading bytecode into VM…",
                        detail=f"{plan.instruction_count()} instructions")
        time.sleep(0.05)
        progress.update(vm_task, advance=1, description="Executing tape ceremony…", detail="cells 0–6")
        time.sleep(0.05)
        progress.update(vm_task, advance=1, description="Loading domain registers…",
                        detail="WALLET · RECV · AMOUNT · GAS · MEMO")
        time.sleep(0.05)

        try:
            intent = plan._compile_and_exec(args.trace) if hasattr(plan, "_compile_and_exec") else None
            if intent is None:
                # Normal path
                compiler_obj = MasterCompiler(verbose=args.verbose)
                intent = compiler_obj.execute_plan(plan, trace=args.trace)
        except Exception as e:
            progress.update(vm_task, advance=1, description=f"[{C_ERR}]VM error[/{C_ERR}]", detail=str(e)[:40])
            console.print(f"\n  {CROSS} [{C_ERR}]VM error:[/{C_ERR}] {e}")
            sys.exit(1)

        progress.update(vm_task, advance=1, description=f"[{C_MX}]VM halted at EMIT instruction[/{C_MX}]",
                        detail="TransactionIntent ready")
        time.sleep(0.05)
        progress.update(vm_task, advance=1)

    if args.trace and hasattr(plan, "_vm_trace"):
        display_vm_trace(plan._vm_trace)

    display_intent(intent)

    # ── Dispatch MultiversX ────────────────────────────────────────────────────
    try:
        tx_hash = run_adapter_with_progress(intent, args.network, trace=args.trace)
    except AdapterError as e:
        console.print(f"  {CROSS} [{C_ERR}]Adapter error:[/{C_ERR}] {e}")
        sys.exit(1)

    display_success(tx_hash, args.network, compile_ms, intent.amount_atto)


