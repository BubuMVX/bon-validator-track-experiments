        acc_data = run_step(2, lambda: provider.get_account(account.address))
        nonce = acc_data.nonce
        adv(2, "done", f"nonce={nonce}")

        # Step 3: Construct tx
        import json, base64
        from collections import OrderedDict

        memo_clean = intent.memo.rstrip("\x00")
